def main():
    created, skipped = 0, 0
    per_year = {}
    for title, start, end, location, contact, resources in EVENTS:
        if CalendarEvent.objects.filter(title=title, start_date=start).exists():
            skipped += 1
            print(f"SKIP (already exists): {start} {title}")
            continue
        event = CalendarEvent(
            title=title, start_date=start, end_date=end, location=location,
            description="", contact_info=contact, resources=resources,
        )
        event.full_clean()
        event.save()
        created += 1
        per_year[start.year] = per_year.get(start.year, 0) + 1

    print(f"\nCreated {created} events, skipped {skipped}.")
    for year in sorted(per_year):
        print(f"  {year}: {per_year[year]} events")

    linked = Competition.objects.filter(
        calendar_event__isnull=False, year__gte=2016, year__lte=2023,
    ).select_related("calendar_event")
    if linked:
        print("\nCompetitions now linked to historical calendar events:")
        for comp in linked:
            print(f"  {comp} -> {comp.calendar_event.title} ({comp.calendar_event.start_date})")


if __name__ == "__main__":
    main()
